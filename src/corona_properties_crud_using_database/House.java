            rs.insertRow();

            stm.close();
            rs.close();

            stm = con.createStatement(TYPE_SCROLL_INSENSITIVE, CONCUR_UPDATABLE);
            String SQL = "SELECT * FROM House";
            rs = stm.executeQuery(SQL);

            rs.next();
            String house1 = rs.getString("House_no");
            double newRent1 = rs.getDouble("Rent");
            String rent1 = Double.toString(newRent1);
            double newDep1 = rs.getDouble("Deposit");
            String deposit1 = Double.toString(newDep1);
            String status1 = rs.getString("status");
            int newId1 = rs.getInt("Occupant_id");
